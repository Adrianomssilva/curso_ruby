class Calculator

   def add (num1,num2)
      valor = num1 + num2
      puts "o resultado da soma é #{valor}"
   end

   def multi (num1,num2)
      valor = num1 * num2
      puts "o resultado da multiplicação é #{valor}"
   end
   
   def sub (num1,num2)
      valor = num1 - num2
      puts "o resultado da subtração é #{valor}"
   end
   
   def div (num1,num2)
      valor = num1 / num2
      puts "o resultado da divisão é #{valor}"
   end

end
